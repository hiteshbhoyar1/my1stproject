package practice;

public class For_Loop_Study {

	public static void main(String[] args) {
		int sum=0;
		System.out.println("sum of numbers between 1 to 10 is : ");
		for(int i=0; i<=10; i++ )
			sum=sum+i;
		System.out.println(sum);
		
		int add=0;
		System.out.println("sum between numbers 30 to 20 is :");
		for(int a=30; a>=21; a--)
			add=add+a;
			System.out.println(add);
			
			
	}

}
